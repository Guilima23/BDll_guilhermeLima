drop database if exists Parte_IA;
create database Parte_IA;
USE Parte_IA;

CREATE TABLE Comissao_tecnica (
    BID INT PRIMARY KEY AUTO_INCREMENT,
    Nomes VARCHAR(100),
    Sobrenomes varchar(100),
    Nacionalidades VARCHAR(50),
    Idades INT,
    Encargos VARCHAR(100)
);

CREATE TABLE Time (
    Ata_fundacao INT PRIMARY KEY,
    Nome VARCHAR(100),
    Cidade VARCHAR(100),
    data_Fundacao DATE,
    Estadio VARCHAR(100),

    BID INT,

    FOREIGN KEY (BID)
    REFERENCES Comissao_tecnica(BID)
);



CREATE TABLE Jogadores (
    BID INT PRIMARY KEY AUTO_INCREMENT,
    Nome_jogador VARCHAR(100),
    sobrenome_jogador varchar(100),
    Data_nascimento DATE,
    Pais VARCHAR(50),
    Posicao VARCHAR(50),
    Altura_cm INT,
    Numero_camisa INT,
    Valor_transferencia INT,

    Ata_fundacao INT,

    FOREIGN KEY (Ata_fundacao)
    REFERENCES Time(Ata_fundacao)
);



CREATE TABLE Contrato (
    Codigo_contrato INT PRIMARY KEY AUTO_INCREMENT,
    Tipo VARCHAR(50),
    Taxa DECIMAL(10,2),
    Anos_contrato INT,

    BID INT,

    FOREIGN KEY (BID)
    REFERENCES Jogadores(BID)
);



CREATE TABLE Campeonato (
    Num_edicao VARCHAR(10) PRIMARY KEY,
    Jogos INT,
    Vitorias INT,
    Derrotas INT,
    Empates INT,
    Gols_pro INT,
    Gols_contra INT,
    Saldo INT,
    Posicao INT,

    Ata_fundacao INT,

    FOREIGN KEY (Ata_fundacao)
    REFERENCES Time(Ata_fundacao)
);



CREATE TABLE Estatistica (
    Temporada INT PRIMARY KEY,
    Artilheiro VARCHAR(100),
    Mais_assistencias INT,
    Menos_vazado VARCHAR(100),
    Quanti_jogos INT,
    Cartoes_amarelos INT,
    Cartoes_vermelhos INT,

    Jogadores INT,

    FOREIGN KEY (Jogadores)
    REFERENCES Jogadores(BID)
);



CREATE TABLE Partida (
    Rodada INT PRIMARY KEY,
    Gols INT,
    Assistencias INT,
    Cartoes INT,
    Num_defesas INT,
    Divididas INT,

    Num_edicao VARCHAR(10),

    FOREIGN KEY (Num_edicao)
    REFERENCES Campeonato(Num_edicao)
);
